SELECT DATEDIFF(YEAR, '2005/05/11', '2023/07/12') AS Years,
	DATEDIFF(MONTH, '2005/05/11', '2023/07/12') AS Months,
	DATEDIFF(WEEK, '2005/05/11', '2023/07/12') AS Weeks,
	DATEDIFF(DAY, '2005/05/11', '2023/07/12') AS DaysGone,
	DATEDIFF(HOUR, '2005/05/11', '2023/07/12') AS HoursGone,
	DATEDIFF(MINUTE, '2005/05/11', '2023/07/12') AS MinutesGone,
	DATEDIFF(SECOND, '2005/05/11', '2023/07/12') AS SecondsGone